const SET_SIZE = 30;
const TOTAL_SETS = 5;

export const LILAC_WORD_SETS: LilacWordSet[] = Array.from(
  { length: TOTAL_SETS },
  (_, index) => {
    const start = index * SET_SIZE;
    const end = start + SET_SIZE;
    return {
      slug: `${start + 1}-${end}`,
      label: `${start + 1}-${end}`,
      words: LILAC_WORDS.slice(start, end),
    };
  }
);

export const getLilacSetBySlug = (slug: string) =>
  LILAC_WORD_SETS.find((set) => set.slug === slug);

